# Bundle Deployment Script
# Copies all necessary files to a target directory for running outside the repo

param(
    [Parameter(Mandatory=$true)]
    [string]$TargetDir,
    
    [Parameter()]
    [ValidateSet("universal", "cuda", "vulkan")]
    [string]$BuildType = "universal",
    
    [Parameter()]
    [switch]$CreateLauncher,
    
    [Parameter()]
    [switch]$Overwrite
)

$ErrorActionPreference = "Stop"

# Resolve absolute path
$TargetDir = Resolve-Path $TargetDir -ErrorAction SilentlyContinue -ErrorVariable resolveError
if (-not $TargetDir) {
    # Directory doesn't exist, create it
    $TargetDir = $resolveError[0].TargetObject
    Write-Host "Creating target directory: $TargetDir" -ForegroundColor Cyan
    New-Item -ItemType Directory -Path $TargetDir -Force | Out-Null
} else {
    $TargetDir = $TargetDir.Path
}

$ProjectRoot = Split-Path -Parent $PSScriptRoot
$SourceManager = Join-Path $ProjectRoot "src" "manager"
$SourceDist = Join-Path $ProjectRoot "dist" $BuildType

Write-Host "===============================================" -ForegroundColor Green
Write-Host "LLaMA Manager Deployment Bundler" -ForegroundColor Green
Write-Host "===============================================" -ForegroundColor Green
Write-Host "Target:    $TargetDir" -ForegroundColor Gray
Write-Host "Build:     $BuildType" -ForegroundColor Gray
Write-Host ""

# Validate source files exist
if (-not (Test-Path $SourceDist)) {
    Write-Error "Build directory not found: $SourceDist`nRun .\build\build-$BuildType.ps1 first"
    exit 1
}

if (-not (Test-Path (Join-Path $SourceManager "server.js"))) {
    Write-Error "Manager source not found: $SourceManager\server.js"
    exit 1
}

# Check if target has existing files
if ((Test-Path (Join-Path $TargetDir "*")) -and -not $Overwrite) {
    Write-Warning "Target directory is not empty. Use -Overwrite to replace."
    $confirm = Read-Host "Continue anyway? (y/N)"
    if ($confirm -ne 'y') {
        Write-Host "Aborted." -ForegroundColor Red
        exit 1
    }
}

# Create directory structure
$Dirs = @(
    $TargetDir,
    (Join-Path $TargetDir "nLogger"),
    (Join-Path $TargetDir "nLogger\src"),
    (Join-Path $TargetDir "bin"),
    (Join-Path $TargetDir "logs")
)

foreach ($dir in $Dirs) {
    if (-not (Test-Path $dir)) {
        New-Item -ItemType Directory -Path $dir -Force | Out-Null
    }
}

Write-Host "Copying manager files..." -ForegroundColor Cyan

# Copy core manager files (no need for node_modules since zero-dependency)
$ManagerFiles = @(
    "server.js",
    "process.js", 
    "models.js",
    "config.js",
    "package.json"
)

foreach ($file in $ManagerFiles) {
    $source = Join-Path $SourceManager $file
    $dest = Join-Path $TargetDir $file
    if (Test-Path $source) {
        Copy-Item $source $dest -Force
        Write-Host "  ✓ $file" -ForegroundColor Gray
    } else {
        Write-Warning "  ✗ $file not found"
    }
}

# Copy nLogger submodule
Write-Host "Copying nLogger submodule..." -ForegroundColor Cyan
$nLoggerSrc = Join-Path $SourceManager "modules" "nLogger" "src" "logger.js"
$nLoggerDest = Join-Path $TargetDir "nLogger\src\logger.js"
if (Test-Path $nLoggerSrc) {
    Copy-Item $nLoggerSrc $nLoggerDest -Force
    Write-Host "  ✓ nLogger/src/logger.js" -ForegroundColor Gray
} else {
    Write-Error "nLogger submodule not found. Run: git submodule update --init --recursive"
    exit 1
}

# Copy compiled binaries
Write-Host "Copying $BuildType binaries..." -ForegroundColor Cyan
$BinaryFiles = Get-ChildItem $SourceDist -File
$EssentialCount = 0
$EssentialFiles = @("llama-server.exe", "llama.dll", "ggml.dll", "ggml-cuda.dll")

foreach ($file in $BinaryFiles) {
    $dest = Join-Path $TargetDir "bin" $file.Name
    Copy-Item $file.FullName $dest -Force
    Write-Host "  ✓ bin\$($file.Name)" -ForegroundColor Gray
    if ($EssentialFiles -contains $file.Name) {
        $EssentialCount++
    }
}

if ($EssentialCount -lt 2) {
    Write-Warning "Very few binaries copied. Build may be incomplete."
}

# Create default config.json for easy editing
$DefaultConfig = @{
    port = 4080
    serverPort = 4081
    modelsDir = "D:\\# AI Stuff\\LMStudio_Models"
    detachOnShutdown = $false
    llamaServerPath = ".\\bin\\llama-server.exe"
} | ConvertTo-Json -Depth 3

$configPath = Join-Path $TargetDir "config.json"
if (-not (Test-Path $configPath) -or $Overwrite) {
    $DefaultConfig | Set-Content $configPath -Encoding UTF8
    Write-Host "  ✓ config.json (default created)" -ForegroundColor Gray
}

# Create launcher scripts if requested
if ($CreateLauncher) {
    Write-Host "Creating launcher scripts..." -ForegroundColor Cyan
    
    # PowerShell launcher
    $PsLauncher = @'
# LLaMA Manager Launcher
# Generated by bundle-deployment.ps1

$scriptDir = Split-Path -Parent $MyInvocation.MyCommand.Path
Set-Location $scriptDir

# Load config if exists
$configFile = Join-Path $scriptDir "config.json"
if (Test-Path $configFile) {
    $config = Get-Content $configFile | ConvertFrom-Json
    $env:MANAGER_PORT = $config.port
    $env:LLAMA_SERVER_PORT = $config.serverPort
    $env:MODELS_DIR = $config.modelsDir
    $env:DETACH_ON_SHUTDOWN = $config.detachOnShutdown
    $env:LLAMA_SERVER_PATH = Join-Path $scriptDir $config.llamaServerPath
} else {
    # Defaults
    $env:MANAGER_PORT = 4080
    $env:LLAMA_SERVER_PORT = 4081
    $env:LLAMA_SERVER_PATH = Join-Path $scriptDir "bin\llama-server.exe"
}

Write-Host "Starting LLaMA Manager..." -ForegroundColor Green
Write-Host "Port:        $env:MANAGER_PORT" -ForegroundColor Gray
Write-Host "Server Port: $env:LLAMA_SERVER_PORT" -ForegroundColor Gray
Write-Host "Models:      $env:MODELS_DIR" -ForegroundColor Gray
Write-Host ""

node server.js
'@
    
    $launcherPath = Join-Path $TargetDir "start.ps1"
    $PsLauncher | Set-Content $launcherPath -Encoding UTF8
    Write-Host "  ✓ start.ps1" -ForegroundColor Gray
    
    # CMD launcher (simple wrapper)
    $CmdLauncher = @'
@echo off
cd /d "%~dp0"
powershell.exe -ExecutionPolicy Bypass -File start.ps1
'@
    $cmdPath = Join-Path $TargetDir "start.bat"
    $CmdLauncher | Set-Content $cmdPath -Encoding ASCII
    Write-Host "  ✓ start.bat" -ForegroundColor Gray
}

# Create README for the bundle
$Readme = @"
# LLaMA Manager Deployment

This is a standalone deployment of the LLaMA Manager.

## Files

- \`server.js\` - Main manager process
- \`bin/\` - Compiled llama.cpp binaries
- \`logs/\` - Runtime logs
- \`config.json\` - Configuration (edit this)
- \`start.ps1\` - PowerShell launcher
- \`start.bat\` - Windows batch launcher

## Quick Start

1. Edit \`config.json\` to set your models directory
2. Run \`start.bat\` or \`start.ps1\`
3. Access API at http://127.0.0.1:4080

## Configuration

Edit \`config.json\`:

\`\`\`json
{
  "port": 4080,                    // Manager API port
  "serverPort": 4081,              // llama-server port  
  "modelsDir": "D:\\\\Models",      // Your GGUF models directory
  "detachOnShutdown": false,       // Keep model loaded on restart
  "llamaServerPath": ".\\\\bin\\\\llama-server.exe"
}
\`\`\`

Or set environment variables:
- \`MANAGER_PORT\` - Manager port
- \`LLAMA_SERVER_PORT\` - Server port
- \`MODELS_DIR\` - Models directory
- \`DETACH_ON_SHUTDOWN\` - Set to \`true\` to preserve VRAM

## API

| Endpoint | Method | Description |
|----------|--------|-------------|
| /models | GET | List available models |
| /start | POST | Start llama-server with model |
| /stop | POST | Stop server |
| /status | GET | Get current status |

See full documentation in the source repository.

## Updating

To update:
1. Stop the service
2. Run bundle script again with -Overwrite
3. Your config.json will be preserved
"@

$readmePath = Join-Path $TargetDir "README.txt"
$Readme | Set-Content $readmePath -Encoding UTF8
Write-Host "  ✓ README.txt" -ForegroundColor Gray

Write-Host ""
Write-Host "===============================================" -ForegroundColor Green
Write-Host "Bundle complete!" -ForegroundColor Green
Write-Host "===============================================" -ForegroundColor Green
Write-Host "Location: $TargetDir" -ForegroundColor Cyan
Write-Host ""
Write-Host "To run:" -ForegroundColor Yellow
if ($CreateLauncher) {
    Write-Host "  cd '$TargetDir'" -ForegroundColor Gray
    Write-Host "  .\\start.bat" -ForegroundColor Gray
} else {
    Write-Host "  cd '$TargetDir'" -ForegroundColor Gray
    Write-Host "  `$env:MANAGER_PORT = 4080" -ForegroundColor Gray
    Write-Host "  `$env:MODELS_DIR = 'D:\\Models'" -ForegroundColor Gray
    Write-Host "  node server.js" -ForegroundColor Gray
}
Write-Host ""
Write-Host "Edit config.json to customize settings." -ForegroundColor Gray
